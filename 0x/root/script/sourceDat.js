function renderResources() {
    if (!container) return; // Safety check

    const htmlString = resourceData.map(resource => `
        <div class="mb-10">
            <div class="flex justify-between items-baseline">
                <h2 class="text-lg font-semibold">${resource.title}</h2>
                <span class="text-xs font-mono text-gray-600 dark:text-gray-400 bg-gray-100 dark:bg-gray-800 px-2 py-0.5 rounded">
                    ${resource.format}
                </span>
            </div>
            
            <p class="text-sm mt-1">
                <span class="font-medium">Copyright:</span> 
                ${resource.copyright}
            </p>
            
            <p class="text-sm font-mono text-gray-600 dark:text-gray-400 mt-1">
                Source: ${resource.source || 'N/A'}
            </p>

            <details class="mt-2 group">
                <summary class="text-sm text-blue-600 dark:text-blue-400 cursor-pointer list-none flex items-center">
                    <span class="group-open:hidden">&#9660;</span>
                    <span class="hidden group-open:inline">&#9650;</span>
                    <span class="ml-1 underline">expand original source</span>
                </summary>
                <div class="mt-2 p-3 bg-gray-100 dark:bg-gray-800 rounded text-xs break-all font-mono text-gray-700 dark:text-gray-300">
                    <a class="hover:text-[#34A853] hover:font-bold" href="${resource.originalLink}" target="_blank">
                        ${resource.originalLink}
                    </a>
                </div>
            </details>
        </div>
    `).join('');

    container.innerHTML = htmlString;
}

